#include "../includes/ft_printf.h"

void	ft_xx(int c, int *x)
{
	unsigned int	a;
	char			*hex;

	hex = "0123456789ABCDEF";
	a = c;
	if (a >= 16)
	{
		ft_xx(a / 16, x);
	}
	write(1, &hex[a % 16], 1);
	(*x)++;
}
